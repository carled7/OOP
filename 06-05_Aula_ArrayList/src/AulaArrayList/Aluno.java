public class Aluno {
    private String nome;
    private String curso;
    private String endereco;
    private int RA;
    private double mediaHistorico;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCurso() {
        return curso;
    }

    public void setCurso(String curso) {
        this.curso = curso;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public int getRA() {
        return RA;
    }

    public void setRA(int RA) {
        this.RA = RA;
    }

    public double getMediaHistorico() {
        return mediaHistorico;
    }

    public void setMediaHistorico(double mediaHistorico) {
        this.mediaHistorico = mediaHistorico;
    }

    
    
}
